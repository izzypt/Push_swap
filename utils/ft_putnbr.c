#include "../push_swap.h"

void	put_nbr(int number)
{
	if (number < 0)
	{
		write(1, "-", 1);
		number = -number;
	}
	if (number >= 10)
		put_nbr(number / 10);
	write(1, &"0123456789"[number % 10], 1);
}
